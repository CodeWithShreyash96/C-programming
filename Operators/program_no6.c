// *Relational Operator* //
#include<stdio.h>
int main()
{
    int a,b;

    printf("\n Enter the Number a & b :");
    scanf("%d%d",&a,&b);

    printf("\n a<b = %d", a<b);
    printf("\n a>b = %d", a>b);
    printf("\n a<=b = %d", a<=b);
    printf("\n a>=b = %d", a>=b);
    printf("\n a==b = %d", a==b);
    printf("\n a!=b = %d", a!=b);

    return 0;

}

