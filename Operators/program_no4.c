// *Increment Decrement Operator* //
#include<stdio.h>
int main()
{
    int a,b;

    printf("\n Enter the Number a:");
    scanf("%d",&a);

    printf("\n Enter the Number b:");
    scanf("%d",&b);

    printf("\n the increment is : %d", ++a);

    printf("\n the decrement is : %d", --a);

    printf("\n the increment is : %d", ++b);

    printf("\n the decrement is : %d", --b);

    return  0;
}